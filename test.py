bottles_of_beer = 99

while bottles_of_beer > 2:
	print(bottles_of_beer, "bottles of beer on the wall,",
		bottles_of_beer, "bottles of beer!")
	bottles_of_beer -= 1
	print("Take one down, pass it around,", bottles_of_beer,
		"bottles of beer on the wall!")

print("2 bottles of beer on the wall, 2 bottles of beer!",
	"Take one down, pass it around, 1 bottle of beer on the wall!")

print("1 bottle of beer on the wall, 1 bottle of beer!",
	"Take it down, pass it around, no more bottles of beer on the wall!")